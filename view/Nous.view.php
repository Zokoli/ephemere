      <h3> Vulgarisation </h3>
          <p> Le processus de fabrication de la bière se déroule en cinq étapes principales :<br>
              1. Maltage : Le malt est préparé en nettoyant et triant les grains, puis en les trempant dans l'eau pour les faire germer.<br>
              Ensuite, les graines sont séchées et chauffées pour arrêter la germination.<br>
              2. Brassage : Le malt est concassé en moulure et mélangé avec de l'eau chaude lors de l'empâtage. Ensuite, 
              le mélange est agité et filtré pour séparer les grains.<br>
              <br>  On ajoute ensuite le houblon, le nectar de pêche et l'hydrolat 
              de lavande dans le même récipient et on fait bouillir le mélange, suivi d'un refroidissement.<br>
              3. Fermentation primaire (alcoolique) : Des levures sont ajoutées au mélange, ainsi qu'un supplément de sucre.
  </p>
